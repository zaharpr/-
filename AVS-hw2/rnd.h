#ifndef __rnd__
#define __rnd__

#include "stdlib.h"

//------------------------------------------------------------------------------
// rnd.h - содержит генератор случайных дробных чисел.
//------------------------------------------------------------------------------

class Random {
public:
    static double RandomDouble() {
        return 10 + (double) (rand()) / ((double) (RAND_MAX / ((100 - 10) * 1.0)));
    }
};

#endif //__rnd__
